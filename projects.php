                        The traffic congestion in the walled city progressively becomes the major component which would
                        influence the travel or working experience. Revitalization of Walled City will not only promote
                        heritage conservation but also give a strong boost to tourism and consequent economic upliftment
                        of the local population. In the research process, the behavior questionnaire and traffic flow
                        survey as the first quantitative measurements were used for 2 preparation days and 6 working
                        days. The behavior survey was attempted to identify the possible attitude or perspectives of the
                        people in Walled City, Udaipur. The results show that there are more than 50% of the
                        participants express the view of disagree or ambiguous perspectives about the transportation
                        condition nowadays. Moreover, there is nearly 70 percent of the whole local residents it is
                        difficult for them to finding parking in the Walled City. The traffic flow survey tries to
                        generally calculate the potential transport condition at a different time interval on certain
                        days. In the process, the different collection destination was settled and it ensured the
                        accuracy and usefulness of the statistics to some extent.
