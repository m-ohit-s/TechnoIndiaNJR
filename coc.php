                    notification
                </li>
                <li>
                    Should not indulge in agitation and instigation of violence inside
                    or outside institute campus
                </li>
                <li>Boys and girls should use respective toilets only</li>
                <li>
                    Students should be cooperative in keeping the institute campus neat
                    and tidy
                </li>
                <li>Should not damage any institute property</li>
                <li>
                    Interested students can avail bus transport facility and be on time
                </li>

                <li>
                    Students must enter the classroom at least five minutes before the
                    commencement of class
                </li>
                <li>
                    Students should handle computers and other equipment properly in
                    laboratories
                </li>
                <li>
                    Students should seriously and meticulously appear in the weekly test
                    and submit assignments on time
                </li>
                <li>
                    In case of any discontinuity, subject to clearance of fees and
                    proper no dues, the Transfer Certificate will be issued obeying the
                    rules and regulations of Rajasthan Technical University (RTU) Kota
                </li>
                <li>
                    In case of a change in residential address, the student should
                    inform the class coordinator
                </li>
                <li>
                    Students should carry an institute ID card in institute provided
                    transport and also on campus
                </li>
                <li>
                    Students are forbidden to organize tours or meetings or to attend
                    any meeting in the college or to collect money for any purpose
                    without prior permission of the Principal
                </li>
                <li>Unnecessary roaming inside the campus is not permitted</li>
                <li>
                    Students should wear proper institute uniform on Monday, Tuesday,
                    Thursday, and Friday and maintain decency in this perspective
                </li>
                <li>
                    Hostellers must occupy the room allotted to them and must not change
                    the room without the permission of the warden. They have to keep
                    their rooms clean and hygienic
                </li>
                <li>
                    No student is allowed to stay in the hostel during college hours
                    without prior permission of the warden
                </li>
                <li>
                    Inmates should not involve themselves in smoking, drinking,
                    gambling, writing on walls and doors, etc.
                </li>
                <li>
                    Hostellers will be held responsible for the damage to the rooms they
                    occupy and items like a fan, furniture, tube light, switches, toilet
                    sink, etc. In case of damage or disfigurement of any hostel
                    property, they will have to pay the cost as fixed by the Principal
                    and the warden.
                </li>
                <li>
                    Hostellers are suggested not to bring expensive items. In case of
                    loss, the institute management is not responsible
                </li>
                <li>
                    The rooms should be locked whenever the students are away and the
                    room keys should not be given to anyone except the warden.
                </li>
                <li>
                    Boys are not allowed to enter the girl’s hostel and vice versa
                </li>
                <li>Ragging is strictly prohibited inside the campus</li>
                <li>Use of plastic is not allowed inside the campus</li>
