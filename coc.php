                    In case of medical leave, the student has to submit a medical
                    certificate on the day of arrival
                </li>
                <li>
                    Use of drugs, tobacco products, smoking is not permitted in the
                    institute premises
                </li>
                <li>
                    Ragging and eve-teasing are not allowed under any circumstances and
